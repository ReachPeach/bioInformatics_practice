#define dist(a, b) BLOSUM62[num[a]][num[b]]

void LCS(const string &s, const string &t) {
    int n = s.size(), m = t.size();
    vector<vector<long long>> lcs(n + 1, vector<long long>(m + 1, INT64_MIN));
    int d = -5;
    for (int i = 0; i <= n; i++) lcs[i][0] = d * i;
    for (int i = 0; i <= m; i++) lcs[0][i] = d * i;

    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            lcs[i][j] = max(lcs[i - 1][j - 1] + dist(s[i - 1], t[j - 1]),
                            max(lcs[i - 1][j], lcs[i][j - 1]) + d);
        }
    }
    cout << lcs[n][m];

    string ansA, ansB;
    while (n > 0 || m > 0) {
        if (lcs[n][m] == lcs[n - 1][m - 1] + dist(s[n - 1], t[m - 1])) {
            ansA = s[n - 1] + ansA;
            ansB = t[m - 1] + ansB;
            n--;
            m--;
        } else if (lcs[n][m] == lcs[n - 1][m] + d) {
            ansA = s[n - 1] + ansA;
            ansB = '-' + ansB;
            n--;
        } else {
            ansA = '-' + ansA;
            ansB = t[m - 1] + ansB;
            m--;
        }
    }
    while (n > 0) {
        ansA = s[n - 1] + ansA;
        ansB = '-' + ansB;
        n--;
    }
    while (m > 0) {
        ansA = '-' + ansA;
        ansB = t[m - 1] + ansB;
        m--;
    }
    cout << '\n' << ansA << '\n' << ansB;
}

